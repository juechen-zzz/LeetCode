class NestedIterator:
    def __init__(self, nestedList: [NestedInteger]):
        def flatten(num):
            if num.isInteger():
                self.numList.append(num)
            else:
                for n in num.getList():
                    flatten(n)
                    
        self.numList = []
        self.idx = 0
        for num in nestedList:
            flatten(num)
        
    def next(self) -> int:
        self.idx += 1
        return self.numList[self.idx - 1]
    
    def hasNext(self) -> bool:
        return True if self.idx < len(self.numList) else False
         

